let number1 = parseInt(prompt("Digite o primeiro número: "));
let number2 = parseInt(prompt("Digite o segundo número: "));

if (number1 > number2) {

    alert("Número 1 é maior que Número 2");
}

else if (number1 == number2) {

    alert("Os dois números sao iguais");
}

else {
    alert("Número 2 é maior que Número 1");
}

